public class UnivDetail {
    String objectID;
    String univName;
    String UnivCountry;
    String Ranking;
    String univRating;
    String univLogo;

    public String getObjectID() {
        return objectID;
    }

    public void setObjectID(String objectID) {
        this.objectID = objectID;
    }

    public String getUnivName() {
        return univName;
    }

    public void setUnivName(String univName) {
        this.univName = univName;
    }

    public String getUnivCountry() {
        return UnivCountry;
    }

    public void setUnivCountry(String univCountry) {
        UnivCountry = univCountry;
    }

    public String getRanking() {
        return Ranking;
    }

    public void setRanking(String ranking) {
        Ranking = ranking;
    }

    public String getUnivRating() {
        return univRating;
    }

    public void setUnivRating(String univRating) {
        this.univRating = univRating;
    }

    public String getUnivLogo() {
        return univLogo;
    }

    public void setUnivLogo(String univLogo) {
        this.univLogo = univLogo;
    }
}
